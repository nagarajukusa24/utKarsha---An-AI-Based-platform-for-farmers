<h1 class="display-4"><span style="color:white">Paramparagat Krishi Vikas Yojana (PKVY)</span></h1>
<div class="jumbotron">
  <p class="lead">The Paramparagat Krishi Vikas Yojana (PKVY), an initiative to promote organic farming in the country, was launched by the NDA government in 2015.

According to the scheme, farmers will be encouraged to form groups or clusters and take to organic farming methods over large areas in the country.

The aim is to form 10,000 clusters over the next three years and bring about five lakh acres of agricultural area under organic farming. The government also intends to cover the certification costs and promote organic farming through the use of traditional resources.

To avail the scheme, each cluster or group must have 50 farmers willing to take up organic farming under the PKVY and possess a total area of at least 50 acres. Each farmer enrolling in the scheme will be provided INR 20,000 per acre by the government spread over three years time.</p>
